# mathematical operations

# rounding
x = 5
y = 3
z = round(x/y, 2)
print(z)

# modulus
print(5 % 3)

# exponent
print(5 ** 3)

# floor division
print(15 // 4)

# order of operations
print(3 + 4 * 5)
print((3 + 4) * 5)

# increment
x = 5
x += 1
print(x)

# decrement
x = 10
x -= 1
print(x)

# absolute value
x = -50
print(abs(x))

# max
print(max(5, 10))

# min
print(min(5, 10))

# round
print(round(3.14159, 2))

# math module
import math
print(math.floor(3.67))
print(math.ceil(3.44))
print(math.sqrt(36))
print(math.pow(2, 3))