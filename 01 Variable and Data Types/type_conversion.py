# Type conversion is the process of converting one data type to another.
# The conversion can be done either implicitly or explicitly.
# Implicit conversion is done by the Python interpreter without programmer intervention.
# Explicit conversion is done by the programmer using built-in functions.

# Implicit conversion
# Python automatically converts one data type to another data type.
# Example: int to float, float to complex, int to complex, etc.
# The following example converts an integer to a float.
age = 20
print(age)
print(type(age))
age_float = float(age)
print(age_float)
print(type(age_float))

# Python provides built-in functions for explicit conversion.
# The following example converts a float to an integer.
price = 9.99
print(price)
print(type(price))
price_int = int(price)
print(price_int)
print(type(price_int))

# The following example converts a string to an integer.
age = '20'
print(age)
print(type(age))
age_int = int(age)
print(age_int)
print(type(age_int))

