# fstrings are a new way to format strings in python 3.6 and above versions
# fstrings are faster than format() method
# fstrings are more readable than format() method

name = "Rajshahi"
country = "Bangladesh"
message = f"{name} is a city of {country}"
print(message)

# fstrings can be used with any data types
# fstrings can be used with any expressions, functions, methods etc.

age = 20
message = f"Your age is {age}"
print(message)