# Integer in python
print(3)
print(type(3)) # <class 'int'>

# sum of two integers
print(3 + 4) # 7

# Float in python
print(4.5)
print(type(4.5)) # <class 'float'>
print(3.5 + 4.5) # 8.0


# Complex numbers in python
# Complex numbers are written in the form x + yj
# x is the real part
# y is the imaginary part
# j is the imaginary unit
print(3 + 4j)
print(type(3 + 4j)) # <class 'complex'>
print(3 + 4j + 5 + 6j) # (8+10j)

