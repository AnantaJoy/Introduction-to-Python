# Strings are a data type in Python that are used to store text. 
# Strings are immutable, which means that once a string is created, it cannot be changed.
# Strings can be created using single quotes('') or double quotes("").

names = "John"
print(names)
print(type(names))


# strings using single quotes
names = 'John'
print(names)
print(type(names))

# conctenation of strings
# Strings can be concatenated using the + operator.
first_name = "Bangla"
last_name = "desh"
full_name = first_name+ last_name
print(full_name)

